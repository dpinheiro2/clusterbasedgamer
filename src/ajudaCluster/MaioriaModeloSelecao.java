package ajudaCluster;

public class MaioriaModeloSelecao {
    int chamouEperdeu;
    int chamouEganhou;
    int naoChamou;
    
    
    
	public MaioriaModeloSelecao() {
		super();
		this.chamouEperdeu = 0;
		this.chamouEganhou = 0;
		this.naoChamou = 0;
	}
	public int getChamouEperdeu() {
		return chamouEperdeu;
	}
	public void setChamouEperdeu(int chamouEperdeu) {
		this.chamouEperdeu = chamouEperdeu;
	}
	public int getChamouEganhou() {
		return chamouEganhou;
	}
	public void setChamouEganhou(int chamouEganhou) {
		this.chamouEganhou = chamouEganhou;
	}
	public int getNaoChamou() {
		return naoChamou;
	}
	public void setNaoChamou(int naoChamou) {
		this.naoChamou = naoChamou;
	}
    
    
}
